int8_t song[] = { -3, 2, 5, 4, 2, 5, 2, 4, 2, -2, 0, -3 };
const uint8_t noteCount = sizeof(song) / sizeof(song[0]);
uint32_t ReadPotentiometer(void)
{
  HAL_ADC_Start(&hadc1);
  if (HAL_ADC_PollForConversion(&hadc1, 100) == HAL_OK)
    return HAL_ADC_GetValue(&hadc1);
  HAL_ADC_Stop(&hadc1);
  return 0;
}

float CalculateFrequency(int8_t n)
{
  return BASE_FREQ * pow(SEMITONE_RATIO, n);
}

uint16_t CalculateDutyCycle(uint16_t arr, float volumePercent)
{
  uint16_t minDuty = arr / 10;
  uint16_t maxDuty = arr * 9 / 10;
  return minDuty + (uint16_t)((maxDuty - minDuty) * volumePercent);
}

uint32_t GetAverageAdcValue(uint8_t samples)
{
  uint32_t sum = 0;
  for (uint8_t i = 0; i < samples; i++)
  {
    sum += ReadPotentiometer();
    HAL_Delay(1);
  }
  return sum / samples;
}

void PlayNote(int8_t semitoneOffset)
{
  float freq = CalculateFrequency(semitoneOffset);
  uint16_t arr = (uint16_t)(72000000 / freq) - 1;

  __HAL_TIM_SET_AUTORELOAD(&htim1, arr);

  uint32_t adcVal = GetAverageAdcValue(5);
  float volume = (adcVal / 4095.0f);
  volume *= volume;

  uint16_t duty = CalculateDutyCycle(arr, volume);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, duty);

  HAL_Delay(400); // Note duration
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
  HAL_Delay(DELAY_BETWEEN_NOTES);
}



