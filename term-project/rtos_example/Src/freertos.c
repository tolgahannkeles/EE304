void StartLCDTask(void *argument) {
    LCD_Init();
    LCD_Set_Cursor(0, 0);
    LCD_Send_String("Hello STM32!");
    LCD_Set_Cursor(1, 0);
    LCD_Send_String("FreeRTOS Ready");

    for (;;) {
        osDelay(1000);
    }
}

